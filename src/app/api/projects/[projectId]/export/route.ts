function escapeHtml(str: string) {
  return str.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
}

function markdownToHtml(md: string): string {
  const lines = md.split("\n");
  const out: string[] = [];
  let i = 0;
  let inTable = false;
  let tableRows: string[][] = [];

  function flushTable() {
    if (!tableRows.length) return;
    const header = tableRows[0];
    const body = tableRows.slice(2);
    out.push("<table>");
    out.push("<thead><tr>" + header.map((h) => `<th>${processInline(h)}</th>`).join("") + "</tr></thead>");
    out.push("<tbody>");
    body.forEach((row) => {
      out.push("<tr>" + row.map((c) => `<td>${processInline(c)}</td>`).join("") + "</tr>");
    });
    out.push("</tbody></table>");
    tableRows = [];
    inTable = false;
  }

  while (i < lines.length) {
    const line = lines[i];

    // Mermaid code block — keep as-is for Mermaid.js
    if (line.startsWith("```mermaid")) {
      if (inTable) flushTable();
      const mermaidLines: string[] = [];
      i++;
      while (i < lines.length && !lines[i].startsWith("```")) {
        mermaidLines.push(lines[i]);
        i++;
      }
      out.push(`<div class="mermaid">${mermaidLines.join("\n")}</div>`);
      i++;
      continue;
    }

    // Other code blocks
    if (line.startsWith("```")) {
      if (inTable) flushTable();
      const codeLines: string[] = [];
      i++;
      while (i < lines.length && !lines[i].startsWith("```")) {
        codeLines.push(escapeHtml(lines[i]));
        i++;
      }
      out.push(`<pre><code>${codeLines.join("\n")}</code></pre>`);
      i++;
      continue;
    }

    // Table
    if (line.startsWith("|")) {
      inTable = true;
      const cells = line.split("|").filter((_, idx, arr) => idx > 0 && idx < arr.length - 1).map((c) => c.trim());
      tableRows.push(cells);
      i++;
      continue;
    }

    if (inTable && !line.startsWith("|")) flushTable();

    // HR
    if (/^---+$/.test(line.trim())) {
      out.push('<hr class="doc-divider">');
      i++;
      continue;
    }

    // Headings — H1 gets special doc-header treatment
    if (line.startsWith("# ")) {
      const text = line.slice(2);
      const id = text.toLowerCase().replace(/[^a-z0-9]+/g, "-");
      // First H1 is the doc title — wrap in doc-header
      out.push(`<div class="doc-section"><div class="doc-header"><h1 id="${id}">${processInline(text)}</h1></div>`);
      // Find the closing HR and wrap all content
      i++;
      continue;
    }
    if (line.startsWith("## ")) {
      const text = line.slice(3);
      const id = text.toLowerCase().replace(/[^a-z0-9]+/g, "-");
      out.push(`<h2 id="${id}">${processInline(text)}</h2>`);
      i++;
      continue;
    }
    if (line.startsWith("### ")) {
      out.push(`<h3>${processInline(line.slice(4))}</h3>`);
      i++;
      continue;
    }
    if (line.startsWith("#### ")) {
      out.push(`<h4>${processInline(line.slice(5))}</h4>`);
      i++;
      continue;
    }

    // Blockquote
    if (line.startsWith("> ")) {
      out.push(`<blockquote>${processInline(line.slice(2))}</blockquote>`);
      i++;
      continue;
    }

    // Unordered list
    if (/^[-*] /.test(line)) {
      const items: string[] = [];
      while (i < lines.length && /^[-*] /.test(lines[i])) {
        items.push(`<li>${processInline(lines[i].replace(/^[-*] /, ""))}</li>`);
        i++;
      }
      out.push("<ul>" + items.join("") + "</ul>");
      continue;
    }

    // Ordered list
    if (/^\d+\. /.test(line)) {
      const items: string[] = [];
      while (i < lines.length && /^\d+\. /.test(lines[i])) {
        items.push(`<li>${processInline(lines[i].replace(/^\d+\. /, ""))}</li>`);
        i++;
      }
      out.push("<ol>" + items.join("") + "</ol>");
      continue;
    }

    // Checkbox
    if (/^- \[[ x]\]/.test(line)) {
      const checked = line.includes("[x]");
      const text = line.replace(/^- \[[ x]\] /, "");
      out.push(`<p><label><input type="checkbox" ${checked ? "checked" : ""}> ${processInline(text)}</label></p>`);
      i++;
      continue;
    }

    // Empty line
    if (line.trim() === "") {
      out.push("");
      i++;
      continue;
    }

    // Paragraph
    out.push(`<p>${processInline(line)}</p>`);
    i++;
  }

  if (inTable) flushTable();
  return out.join("\n");
}

function processInline(text: string): string {
  return text
    .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
    .replace(/\*([^*]+)\*/g, "<em>$1</em>")
    .replace(/`([^`]+)`/g, "<code>$1</code>")
    .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>')
    .replace(/☐/g, "☐")
    .replace(/☑/g, "☑");
}

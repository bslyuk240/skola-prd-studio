import { auth } from "@clerk/nextjs/server";
import { NextRequest, NextResponse } from "next/server";
import { db } from "@/db";
import { projects, documents, buildTasks, securityChecks } from "@/db/schema";
import { eq, and } from "drizzle-orm";

const DOC_ORDER = [
  "prd",
  "trd",
  "app_flow",
  "ux_brief",
  "backend_schema",
  "implementation_plan",
  "security_blueprint",
] as const;

const DOC_TITLES: Record<string, string> = {
  prd: "Product Requirements Document",
  trd: "Technical Requirements Document",
  app_flow: "App Flow",
  ux_brief: "UI/UX Design Brief",
  backend_schema: "Backend Schema",
  implementation_plan: "Implementation Plan",
  security_blueprint: "Security Blueprint",
};

interface Params {
  params: Promise<{ projectId: string }>;
}

export async function GET(req: NextRequest, { params }: Params) {
  const { projectId } = await params;
  const { userId } = await auth();
  if (!userId) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const format = req.nextUrl.searchParams.get("format") ?? "markdown";

  const [project] = await db
    .select()
    .from(projects)
    .where(and(eq(projects.id, projectId), eq(projects.userId, userId)))
    .limit(1);
  if (!project) return NextResponse.json({ error: "Not found" }, { status: 404 });

  const docs = await db.select().from(documents).where(eq(documents.projectId, projectId));
  const tasks = await db.select().from(buildTasks).where(eq(buildTasks.projectId, projectId));

  const exportDate = new Date().toLocaleDateString("en-GB", { day: "numeric", month: "long", year: "numeric" });

  // ─── Build Markdown content ───────────────────────────────────────────
  const mdParts: string[] = [];

  // Cover
  mdParts.push(`# ${project.name} — Complete Blueprint`);
  mdParts.push(`**Generated by SkolaTech PRD Studio**  `);
  mdParts.push(`**Date:** ${exportDate}  `);
  mdParts.push(`**Security Level:** ${project.securityLevel ?? "standard"}  `);
  mdParts.push(`**Blueprint Readiness:** ${project.readinessScore ?? 0}%  `);
  mdParts.push(`**Security Score:** ${project.securityScore ?? 0}/100`);
  mdParts.push("");
  mdParts.push("---");
  mdParts.push("");

  // Stack summary
  const stack = (project.stackPreferences as Record<string, string>) ?? {};
  const stackRows = Object.entries(stack).filter(([, v]) => v && v !== "None");
  if (stackRows.length > 0) {
    mdParts.push("## Technology Stack");
    mdParts.push("");
    mdParts.push("| Layer | Choice |");
    mdParts.push("|-------|--------|");
    stackRows.forEach(([k, v]) => mdParts.push(`| ${k.charAt(0).toUpperCase() + k.slice(1)} | ${v} |`));
    mdParts.push("");
    mdParts.push("---");
    mdParts.push("");
  }

  // Table of contents
  mdParts.push("## Table of Contents");
  mdParts.push("");
  DOC_ORDER.forEach((type, i) => {
    const doc = docs.find((d) => d.type === type);
    const status = doc?.status === "ready" || doc?.status === "approved" ? "✅" : "⏳";
    mdParts.push(`${i + 1}. ${status} [${DOC_TITLES[type]}](#${DOC_TITLES[type].toLowerCase().replace(/[^a-z0-9]+/g, "-")})`);
  });
  if (tasks.length > 0) mdParts.push(`${DOC_ORDER.length + 1}. 📋 [Build Task Roadmap](#build-task-roadmap)`);
  mdParts.push("");
  mdParts.push("---");
  mdParts.push("");

  // Documents
  for (const type of DOC_ORDER) {
    const doc = docs.find((d) => d.type === type);
    mdParts.push(`# ${DOC_TITLES[type]}`);
    mdParts.push("");
    if (doc?.content) {
      mdParts.push(doc.content);
    } else {
      mdParts.push(`> ⏳ This document has not been generated yet.`);
    }
    mdParts.push("");
    mdParts.push("---");
    mdParts.push("");
  }

  // Build tasks
  if (tasks.length > 0) {
    mdParts.push("# Build Task Roadmap");
    mdParts.push("");
    mdParts.push(`Total tasks: **${tasks.length}** | Done: **${tasks.filter((t) => t.status === "done").length}** | In Progress: **${tasks.filter((t) => t.status === "in_progress").length}**`);
    mdParts.push("");

    const phases = [...new Set(tasks.map((t) => t.phase ?? "General"))];
    for (const phase of phases) {
      const phaseTasks = tasks.filter((t) => (t.phase ?? "General") === phase);
      mdParts.push(`## ${phase}`);
      mdParts.push("");
      mdParts.push("| Task | Priority | Status | Effort |");
      mdParts.push("|------|----------|--------|--------|");
      phaseTasks.forEach((t) => {
        mdParts.push(`| ${t.title} | ${t.priority ?? "medium"} | ${t.status ?? "backlog"} | ${t.estimatedEffort ?? "—"} |`);
      });
      mdParts.push("");
    }
  }

  const markdownContent = mdParts.join("\n");

  if (format === "markdown") {
    const slug = project.name.toLowerCase().replace(/[^a-z0-9]+/g, "-");
    return new NextResponse(markdownContent, {
      headers: {
        "Content-Type": "text/markdown; charset=utf-8",
        "Content-Disposition": `attachment; filename="${slug}-blueprint.md"`,
      },
    });
  }

  // ─── HTML export ─────────────────────────────────────────────────────
  const htmlContent = buildHtmlExport(project.name, exportDate, markdownContent, {
    readinessScore: project.readinessScore ?? 0,
    securityScore: project.securityScore ?? 0,
    securityLevel: project.securityLevel ?? "standard",
    stack: stackRows,
  });

  const slug = project.name.toLowerCase().replace(/[^a-z0-9]+/g, "-");
  return new NextResponse(htmlContent, {
    headers: {
      "Content-Type": "text/html; charset=utf-8",
      "Content-Disposition": `attachment; filename="${slug}-blueprint.html"`,
    },
  });
}

